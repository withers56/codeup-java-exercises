package main;

public class StringExercise {
    public static void main(String[] args) {

        String phrase = "We dont need no";

        System.out.println(phrase + " education");
        System.out.println(phrase + " thought control");

        String secondPhrase = "Check \"this\" out, \"s inside of \"s!";

        System.out.println(secondPhrase);

        String thirdPhrase = "In windows, the main drive is usually C:\\";

        System.out.println(thirdPhrase);

        String fourthPhrase = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";

        System.out.println(fourthPhrase);

    }
}
